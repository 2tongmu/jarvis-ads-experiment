# net_parse.py
# Pipeline stage 2: parse (circuit)_prep.net --> (circuit)_ads_import.net
#
# Reads the @BLOCK / @PDK_SWAP / @KEEP tags from the prepared netlist and
# produces a clean ADS-ready netlist where:
#   - @PDK_SWAP blocks: ideal components replaced by PDK cell instantiation
#   - @KEEP blocks: ideal components passed through unchanged
#   - @BLOCK type=PORT / SIM_CTRL: passed through unchanged
#
# PDK component netlist syntax (from ADS generate_netlist() on WIN_PP1029_DESIGN_KIT):
#
#   2-port FET (WIN_PP1029_MS):
#     "PP1029_MS_PDK":Q1a  <drain> <source>  NOF=2  UGW=80 um  Temp=25  LayoutOption=1  NOISE=1
#
#   3-port FET (WIN_PP1029_CPW):
#     "PP1029_CPW_PDK":Q1a  <drain> <source> <gate>  NOF=2  UGW=80 um  Temp=25  NOISE=1  S_Deb=1
#
#   TFR resistor (PP1029_TFR):
#     PP1029_TFR:Rg1  <n1> <n2>  W=50 um  L=<length> um  T=25
#     Sheet resistance = 50 Ohm/sq, so R = 50 * (L/W)
#     For R=300 Ohm: L/W = 6, e.g. W=50um L=300um
#     For R=47 Ohm:  L/W ~ 1, e.g. W=50um L=50um (closest to 47 Ohm)
#
#   MIM capacitor (PP1029_MIM_CAP_Custom):
#     PP1029_MIM_CAP_Custom:C1  <n1> <n2>  CornerRadius=0.0 um  PointOfCorner=1
#                               Area=0  Perimeter=0  C=<value> fF
#
# Usage:
#   python3 net_parse.py spdt_switch_prep.net
#   --> produces spdt_switch_ads_import.net

import sys, re
from pathlib import Path

# ── PDK component templates ───────────────────────────────────────────────────

def pdk_ms(name, node1, node2, nof, ugw):
    """WIN_PP1029_MS: 2-port pHEMT (drain, source). Source pre-grounded internally.
    NOTE: Do NOT use for switch FETs -- use pdk_cpw instead."""
    return (f'"PP1029_MS_PDK":{name}  {node1} {node2}'
            f'  NOF={nof}  UGW={ugw} um  Temp=25  LayoutOption=1  NOISE=1')

def pdk_cpw(name, node_drain, node_source, node_gate, nof, ugw):
    """WIN_PP1029_CPW: 3-port pHEMT.
    CONFIRMED pin order from symbol probe (ADS 2026):
      P1 = Gate   (left pin,    snap 0.0,  0.0)
      P2 = Drain  (top pin,     snap 0.5, +0.5)
      P3 = Source (bottom pin,  snap 0.5, -0.5)
    Netlist port order: gate  drain  source  (P1 P2 P3)
    """
    return (f'"PP1029_CPW_PDK":{name}  {node_gate} {node_drain} {node_source}'
            f'  NOF={nof}  UGW={ugw} um  Temp=25  NOISE=1  S_Deb=1')

def pdk_tfr(name, node1, node2, r_ohm):
    """PP1029_TFR: thin-film resistor. Sheet R = 50 Ohm/sq.
    Choose W=50um, L = R/50 * 50 um = R um. Cap at L=300um for R>300."""
    w = 50
    l = max(10, min(int(round(r_ohm)), 500))   # L = R um for W=50um (50 Ohm/sq)
    return (f'PP1029_TFR:{name}  {node1} {node2}'
            f'  W={w} um  L={l} um  T=25')

def pdk_mim(name, node1, node2, c_fF):
    """PP1029_MIM_CAP_Custom: MIM capacitor."""
    return (f'PP1029_MIM_CAP_Custom:{name}  {node1} {node2}'
            f'  CornerRadius=0.0 um  PointOfCorner=1  Area=0  Perimeter=0  C={c_fF} fF')

# ── Parser ────────────────────────────────────────────────────────────────────

def parse_tag(line, tag):
    """Extract key=value pairs from a comment tag line."""
    m = re.search(rf'@{tag}\s+(.*)', line)
    if not m:
        return {}
    result = {}
    # match key=value or key="value with spaces"
    for kv in re.findall(r'(\w+)="([^"]*)"', m.group(1)):
        result[kv[0]] = kv[1]
    for kv in re.findall(r'(\w+)=([^\s"]+)', m.group(1)):
        if kv[0] not in result:
            result[kv[0]] = kv[1]
    return result

def parse_prep_netlist(path: Path) -> str:
    lines = path.read_text(encoding="utf-8").splitlines()

    out = []
    out.append(f"; {'='*68}")
    out.append(f"; {path.stem.replace('_prep','')}_ads_import.net")
    out.append(f"; Generated by net_parse.py from: {path.name}")
    out.append(f"; Pipeline stage: 2 of 3  (prepared .net -> ADS import .net)")
    out.append(f"; PDK: WIN_PP1029_DESIGN_KIT")
    out.append(f";   FET (switch): PP1029_CPW_PDK (WIN_PP1029_CPW) -- 3-port, drain/source/gate")
    out.append(f";                 Common-gate switch topology.")
    out.append(f";                 Port order: drain  source  gate")
    out.append(f";                 Gate connected to DC bias via Rg isolation resistor.")
    out.append(f";   FET (2-port): PP1029_MS_PDK  (WIN_PP1029_MS) -- source pre-grounded, NOT used here")
    out.append(f";   Resistor:     PP1029_TFR     (50 Ohm/sq NiCr TFR)")
    out.append(f";   Capacitor:    PP1029_MIM_CAP_Custom")
    out.append(f"; {'='*68}")
    out.append(f";")
    out.append(f"#include \"{Path(r'C:/Users/jarvis/ads_projects/design_kits/WIN_PP1029_DESIGN_KIT/circuit/models/common.net').as_posix()}\"")
    out.append(f"#uselib \"WIN_PP1029_DESIGN_KIT\"")
    out.append(f";")

    i = 0
    while i < len(lines):
        line = lines[i]
        stripped = line.strip()

        # ── @BLOCK open tag ───────────────────────────────────────────────────
        if '@BLOCK' in stripped and '@END_BLOCK' not in stripped and 'name=' in stripped:
            block_meta   = parse_tag(stripped, 'BLOCK')
            block_name   = block_meta.get('name', 'unknown')
            block_type   = block_meta.get('type', '')
            block_desc   = block_meta.get('desc', '')

            # Collect block body lines until @END_BLOCK
            body_lines   = []
            swap_tag     = {}
            keep_comps   = []
            i += 1
            while i < len(lines):
                bline = lines[i].strip()
                if f'@END_BLOCK' in bline and block_name in bline:
                    i += 1
                    break
                if '@PDK_SWAP' in bline:
                    # Accumulate multi-line @PDK_SWAP tag (continuation lines start with ';')
                    swap_raw = bline
                    j = i + 1
                    while j < len(lines):
                        nxt = lines[j].strip()
                        if nxt.startswith(';') and '@' not in nxt and '=' in nxt:
                            swap_raw += ' ' + nxt.lstrip(';').strip()
                            j += 1
                        else:
                            break
                    swap_tag = parse_tag(swap_raw, 'PDK_SWAP')
                elif '@KEEP' in bline:
                    km = re.search(r'components=([\w,]+)', bline)
                    if km:
                        keep_comps = km.group(1).split(',')
                else:
                    body_lines.append(lines[i])
                i += 1

            # ── Emit block ────────────────────────────────────────────────────
            out.append(f";")
            out.append(f"; -- {block_name}  [{block_type}]  {block_desc}")

            # GBIAS blocks: skip for now (added after FET connections verified)
            if block_name.startswith('GBIAS_'):
                out.append(f"; -- {block_name} skipped (add gate bias after FET verification)")
                continue

            # PORT and SIM_CTRL: pass through as-is
            if block_type in ('PORT', 'SIM_CTRL'):
                for bl in body_lines:
                    if bl.strip() and not bl.strip().startswith(';'):
                        out.append(bl.rstrip())
                continue

            # PASSIVE_NETWORK / TLINE with @KEEP: pass components through
            if swap_tag == {} and keep_comps:
                for bl in body_lines:
                    s = bl.strip()
                    if s and not s.startswith(';'):
                        out.append(bl.rstrip())
                continue

            # TRANSISTOR with @PDK_SWAP: emit PDK component(s)
            if swap_tag:
                model     = swap_tag.get('model', '')
                params    = swap_tag.get('params', '')
                port1     = swap_tag.get('port1', '')
                port2     = swap_tag.get('port2', '')
                port3     = swap_tag.get('port3', '')
                replaces  = swap_tag.get('replaces', '').split(',')
                keeps     = [k for k in swap_tag.get('keep', '').split(',') if k and k != 'none']

                # Parse NOF and UGW from params string
                nof = re.search(r'NOF=(\d+)', params)
                ugw = re.search(r'UGW=(\d+)', params)
                nof = nof.group(1) if nof else '2'
                ugw = ugw.group(1) if ugw else '80'

                # Emit PDK FET instance
                # Always use WIN_PP1029_CPW (3-port) for switch FETs.
                # WIN_PP1029_MS has source internally grounded -- wrong for switch topology.
                if model in ('WIN_PP1029_CPW', 'WIN_PP1029_MS'):
                    # port3 is the gate node defined in @PDK_SWAP tag
                    gate_node = port3 if port3 else f'ng_{block_name}'
                    if model == 'WIN_PP1029_MS':
                        out.append(f'; WARNING: WIN_PP1029_MS has pre-grounded source.')
                        out.append(f';          Upgrading to WIN_PP1029_CPW for correct switch topology.')
                    out.append(pdk_cpw(block_name, port1, port2, gate_node, nof, ugw))

                # Emit any kept components
                if keeps:
                    out.append(f"; kept passives for {block_name}:")
                    for bl in body_lines:
                        s = bl.strip()
                        if s and not s.startswith(';'):
                            # Extract component name (e.g. "R:Rtrm1" -> "Rtrm1")
                            m = re.match(r'\w+:(\w+)', s)
                            if m and m.group(1) in keeps:
                                out.append(bl.rstrip())
                continue

            # Fallback: pass through unchanged
            for bl in body_lines:
                s = bl.strip()
                if s and not s.startswith(';'):
                    out.append(bl.rstrip())

        else:
            # Lines outside any @BLOCK (shouldn't be many -- pass through)
            if stripped and not stripped.startswith(';'):
                out.append(line.rstrip())
            i += 1

    return "\n".join(out) + "\n"


if __name__ == "__main__":
    inp = Path(sys.argv[1]) if len(sys.argv) > 1 else Path("spdt_switch_prep.net")
    if not inp.exists():
        print(f"ERROR: {inp} not found"); sys.exit(1)
    result = parse_prep_netlist(inp)
    stem = inp.stem.replace('_prep', '')
    out_path = inp.parent / f"{stem}_ads_import.net"
    out_path.write_text(result, encoding="utf-8")
    print(f"Written: {out_path}")
    print()
    print(result)
